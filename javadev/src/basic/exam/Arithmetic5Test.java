package basic.exam;

public class Arithmetic5Test {
	public static void main(String[] args) {
		int i = 5;
		int t = ++i; // i = i +1; 
  					 // t = i;

		System.out.println("i= " + i);
		System.out.println("t= " + t);
		
		i = 5;
		t = --i; // i = i +1; 
  					 // t = i;

		System.out.println("i= " + i);
		System.out.println("t= " + t);
	}
}
